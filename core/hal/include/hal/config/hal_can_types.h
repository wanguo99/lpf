/************************************************************************
 * HAL层 - CAN类型定义和配置
 *
 * 定义CAN硬件接口使用的基础数据类型和默认配置参数
 ************************************************************************/

#ifndef HAL_CAN_TYPES_H
#define HAL_CAN_TYPES_H

#include <stdint.h>

/*
 * ============================================================================
 * CAN默认配置参数
 * ============================================================================
 */

/* CAN默认接口 */
#define HAL_CAN_DEFAULT_INTERFACE "can0"

/* CAN默认波特率 */
#define HAL_CAN_DEFAULT_BAUDRATE 500000 /* 500Kbps */

/* CAN消息最大长度 */
#define HAL_CAN_MAX_DATA_LEN 8

/*
 * ============================================================================
 * CAN帧结构（硬件层）
 * ============================================================================
 */

typedef struct {
	uint32_t can_id; /* CAN标识符 */
	uint8_t dlc; /* 数据长度 (0-8) */
	uint8_t data[8]; /* 数据字节 */
	uint32_t timestamp; /* 时间戳 (可选) */
} hal_can_frame_t;

#endif /* HAL_CAN_TYPES_H */
