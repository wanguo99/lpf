/************************************************************************
 * OSAL - I/O多路复用封装（poll）
 *
 * 功能：
 * - 封装POSIX poll()函数
 * - 提供跨平台的I/O多路复用接口
 * - 1:1映射系统调用，不引入业务逻辑
 *
 * 设计原则：
 * - 使用固定大小类型，避免平台相关类型
 * - 返回值与系统调用保持一致
 * - 便于RTOS移植
 ************************************************************************/

#ifndef OSAL_POLL_H
#define OSAL_POLL_H

#include "osal_platform.h"
#include "osal_types.h"

/*===========================================================================
 * poll事件标志
 *===========================================================================*/

/* 请求的事件（输入） */
#define OSAL_POLLIN      0x0001  /* 有数据可读 */
#define OSAL_POLLPRI     0x0002  /* 有紧急数据可读 */
#define OSAL_POLLOUT     0x0004  /* 可以写入数据 */

/* 返回的事件（输出） */
#define OSAL_POLLERR     0x0008  /* 发生错误 */
#define OSAL_POLLHUP     0x0010  /* 挂断 */
#define OSAL_POLLNVAL    0x0020  /* 无效的文件描述符 */

/*===========================================================================
 * poll数据结构
 *===========================================================================*/

/**
 * @brief poll文件描述符结构
 */
typedef struct {
    int32_t  fd;       /* 文件描述符 */
    int16_t  events;   /* 请求的事件（OSAL_POLLIN/POLLOUT等） */
    int16_t  revents;  /* 返回的事件（由内核填充） */
} osal_pollfd_t;

/*===========================================================================
 * I/O多路复用操作
 *===========================================================================*/

/**
 * @brief 等待文件描述符上的事件
 * @param fds 文件描述符数组
 * @param nfds 数组长度
 * @param timeout 超时时间（毫秒），-1表示永久等待，0表示立即返回
 * @return >0表示就绪的文件描述符数量，0表示超时，-1表示失败
 */
int32_t OSAL_poll(osal_pollfd_t *fds, uint32_t nfds, int32_t timeout);

#endif /* OSAL_POLL_H */
